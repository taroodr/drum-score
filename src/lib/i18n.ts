export const translate = (
  locale: Locale,
  key: string,
  params?: Record<string, string>
) => {
  const text = translations[locale]?.[key] ?? translations.en?.[key] ?? key;
  if (!params) return text;
  return Object.entries(params).reduce(
    (acc, [paramKey, value]) =>
      acc.replaceAll(`{${paramKey}}`, value),
    text
  );
};
